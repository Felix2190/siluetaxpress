$xajax = new xajax();


function mostrarTabla($informacion)
{
    $r = new xajaxResponse();
    $arrTitulos=array("Paciente","&iquest;Asisti&oacute; a su cita?");
    $tabla = "";
    if (count($informacion) > 0) {
        $tabla .= "<table><thead><tr><th colspan='2'>".$arrTitulos[0]."</th><th colspan='2'>".$arrTitulos[1]."</th></tr></thead><tbody>";
        
        foreach ($informacion as $id => $arr)
            $tabla .= "<tr><td colspan='2'>". $arr['fecha'] . " | " . $arr['nombre_paciente'] . " | " . $arr['cabina'] . "</td>
                <td><a onclick='asistencia(\"".$arr['idCita']."\",\"true\",\"".$arr['telefono']."\")'><img src='images/guardar.png' title='Si' style='width: 25px' /></a></td>
                <td><a onclick='asistencia(\"".$arr['idCita']."\",\"false\",\"".$arr['telefono']."\")'><img src='images/cancelarCita2.png' title='No' style='width: 28px' /></a></td></tr>";
            
            $tabla .= "</tbody></table>";
    }
    
    $r->assign('divTabla', 'innerHTML', $tabla);
    return $r;
}
$xajax->registerFunction("mostrarTabla");

$xajax->processRequest();


