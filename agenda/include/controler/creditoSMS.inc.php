$xajax = new xajax();

function mostrarTabla($informacion)
{
    $r = new xajaxResponse();
    
    $tabla = "";
    if (count($informacion) > 0) {
        $tabla .= "<table><thead><tr><th colspan='3'>Tipo de SMS</th><th>Subtotal</th></tr></thead><tbody>";
        foreach ($informacion as $item => $total)
            $tabla .= "<tr><td colspan='3'>" . $item . "</td><td>" . $total . "</td></tr>";
        
        $tabla .= "</tbody></table>";
    }
    
    $r->assign('divTabla', 'innerHTML', $tabla);
    return $r;
}
$xajax->registerFunction("mostrarTabla");


$xajax->processRequest();

