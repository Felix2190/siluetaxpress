$xajax = new xajax();


function mostrarTabla($informacion)
{
    $r = new xajaxResponse();
    $arrTitulos=array("Sucursal","Estado","Municipio","Direcci&oacute;n","Horario (lunes a viernes)","Horario (s&aacute;bado)","Opciones");
    $tabla = "";
    if (count($informacion) > 0) {
        $tabla .= "<table><thead><tr>";
        
        foreach ($arrTitulos as $titulo)
            $tabla .= "<th>$titulo</th>";
        
        $tabla.="</tr></thead><tbody>";
        
        foreach ($informacion as $id => $arr)
            $tabla .= "<tr><td>" . $arr['sucursal'] . "</td><td>" . $arr['NOM_ENT'] . "</td><td>" . $arr['NOM_MUN'] . "</td>
                        <td>" . $arr['direccion'] . "</td><td>" . $arr['entreSemanaEntrada'] . ":00 a " . $arr['entreSemanaSalida'] . ":00</td>
                        <td>" . $arr['sabadoEntrada'] . ":00 a " . $arr['sabadoSalida'] . ":00</td><td>Opciones</td></tr>";
            
            $tabla .= "</tbody></table>";
    }
    
    $r->assign('divTabla', 'innerHTML', $tabla);
    return $r;
}
$xajax->registerFunction("mostrarTabla");

$xajax->registerFunction("");

$xajax->processRequest();


